# Dynamic binding is a process of resolving the function call to be associated with the function calls during runtime. 
# This means that the compiler does not know which method will be called until the program is running. This is in 
# contrast to static binding, where the compiler knows which method will be called at compile time.

# Message passing is a mechanism for communication between objects. It is a way for objects to request services from
# each other by sending messages. Messages are typically passed by calling a method on another object.

