// 1. **Subsequence:**

//    A subsequence is a sequence derived from another sequence by deleting any number of elements (including zero) from
// the original sequence, without changing the order of the remaining elements. In other words, the subsequence retains the 4
//relative order of elements from the original sequence.

//    For example, if you have the sequence `[1, 2, 3, 4]`, some of its subsequences would be `[1, 2, 4]`, `[2, 3]`, `[1, 3, 4]`, and so on.

// 2. **Subsets:**

//    A subset is a set that contains only some of the elements of another set. Unlike subsequences, subsets don't necessarily maintain the 
//order of the elements. A subset can be thought of as a collection of elements selected from a larger collection, where each element can 
//appear at most once in the subset.

//    For example, if you have the set `{1, 2, 3}`, some of its subsets would be `{1, 2}`, `{3}`, `{1, 2, 3}`, and the empty set `{}`.

